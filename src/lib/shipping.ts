/**
 * 住所文字列から都道府県を抽出する
 * 例: "東京都港区南青山..." → "東京都"
 *     "大阪府大阪市..." → "大阪府"
 *     "神奈川県横浜市..." → "神奈川県"
 */
export function extractPrefecture(address: string): string | null {
  if (!address) return null;
  const m = address.match(
    /^(北海道|東京都|大阪府|京都府|神奈川県|和歌山県|鹿児島県|.{2,3}?[都道府県])/
  );
  return m ? m[1] : null;
}

/**
 * 配送料を計算する（税込・円）
 * @returns 税込料金（円）、または計算できない場合は null
 */
export function calcShippingFee(
  carrier: Carrier,
  size: number,
  prefecture: string
): number | null {
  const zone = PREFECTURE_ZONE[prefecture];
  if (zone === undefined) return null;

  if (carrier === "yamato") {
    const row = YAMATO_RATES[size as YamatoSize];
    return row ? row[zone] ?? null : null;
  } else {
    const row = SAGAWA_RATES[size as SagawaSize];
    return row ? row[zone] ?? null : null;
  }
}

/**
 * 税込金額から税抜金額を算出（切り捨て）
 * 消費税 10% 前提
 */
export function toTaxExclusive(taxInclusivePrice: number): number {
  return Math.floor(taxInclusivePrice / 1.1);
}

/**
 * 配送料明細の商品名を生成する
 */
export function shippingItemName(carrier: Carrier, size: number): string {
  return `配送料（${CARRIER_NAMES[carrier]} ${size}サイズ）`;
}
